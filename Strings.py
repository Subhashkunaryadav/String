# Concatenation
string1 = "Hello"
string2 = "World"
string3 = string1 + string2
print(string3) # "HelloWorld"

# Repetition
string4 = string1 * 3
print(string4) # "HelloHelloHello"

# Indexing
string5 = "Hello World"
print(string5[0]) # "H"

# Slicing
print(string5[0:5]) # "Hello"

# Length
print(len(string5)) # 11

# Formatting
name = "John"
print("Hello {}!".format(name)) # "Hello John!"

# Methods
print(string5.upper()) # "HELLO WORLD"
